package bean;

public class Professor {

	private String nome;
	private String matricula;
	private String area;
	private String regimeJuridico;
	private String titulacao;
	private String dataInicioServicoPublico;
	private String lattes;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMatricula() {
		return matricula;
	}
	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getRegimeJuridico() {
		return regimeJuridico;
	}
	public void setRegimeJuridico(String regimeJuridico) {
		this.regimeJuridico = regimeJuridico;
	}
	public String getTitulacao() {
		return titulacao;
	}
	public void setTitulacao(String titulacao) {
		this.titulacao = titulacao;
	}
	public String getDataInicioServicoPublico() {
		return dataInicioServicoPublico;
	}
	public void setDataInicioServicoPublico(String dataInicioServicoPublico) {
		this.dataInicioServicoPublico = dataInicioServicoPublico;
	}
	public String getLattes() {
		return lattes;
	}
	public void setLattes(String lattes) {
		this.lattes = lattes;
	}
}
